#include <stdio.h>
#include <stdlib.h>

typedef struct _node
{
  int data;           // 8 Bytes
  struct _node *next; // 8 Bytes
} Node;

int main(void)
{
  Node *head = NULL;
  Node *tail = NULL;
  Node *cur = NULL;

  Node *newNode = NULL;
  int readData;

  while (1)
  {
    printf("자연수 입력: ");
    scanf("%d", &readData);
    if (readData < 1)
      break;

    newNode = (Node *)malloc(sizeof(Node));
    newNode->data = readData;
    newNode->next = NULL;

    if (head == NULL)
      head = newNode;
    else
      tail->next = newNode;
    tail = newNode;
  }
  printf("\n");

  printf("입력 받은 데이터의 전체 출력");
  if (head == NULL)
  {
    printf("저장된 자연수가 존재하지 않습니다.");
  }
  else
  {
    int curCount = 1;
    cur = head;
    printf("%d 번째 데이터: %d\n", curCount, cur->data);
    curCount++;
    while (cur->next != NULL)
    {
      cur = cur->next;
      printf("%d 번째 데이터: %d\n", curCount, cur->data);
      curCount++;
    }
  }

  if (head == NULL)
  {
    return 0;
  }
  else
  {
    cur = head;

    Node *delNode = head;
    Node *delNextNode = head->next;

    printf("%d 를 삭제합니다. \n", delNode->data);
    free(delNode);

    while (delNextNode != NULL)
    {
      delNode = delNextNode;
      delNextNode = delNextNode->next;

      printf("%d 를 삭제합니다. \n", delNode->data);
      free(delNode);
    }
  }
  return 0;
}
